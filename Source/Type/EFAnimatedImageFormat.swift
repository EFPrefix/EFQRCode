import Foundation
#if canImport(MobileCoreServices)
import MobileCoreServices
#else
import CoreServices
#endif
import UniformTypeIdentifiers

/**
 * Supported animated image formats for QR code generation.
 *
 * This enum defines the animated image formats that can be generated by EFQRCode.
 * Each format has different characteristics in terms of file size, quality, and
 * browser/device compatibility.
 *
 * ## Supported Formats
 *
 * - **GIF**: Widely supported, smaller file size, limited color palette
 * - **APNG**: Better quality, larger file size, modern browser support
 *
 * ## Usage
 *
 * ```swift
 * let generator = try EFQRCode.Generator("Hello World")
 * 
 * // Generate animated GIF
 * let gifData = try generator.toGIFData(width: 200)
 * 
 * // Generate animated PNG
 * let apngData = try generator.toAPNGData(width: 200)
 * ```
 *
 * ## Format Comparison
 *
 * | Format | File Size | Quality | Browser Support | Animation Support |
 * |--------|-----------|---------|-----------------|-------------------|
 * | GIF    | Smaller   | Lower   | Excellent       | Limited           |
 * | APNG   | Larger    | Higher  | Good            | Full              |
 */
enum EFAnimatedImageFormat: CaseIterable {
    /// Graphics Interchange Format (GIF).
    case gif
    /// Animated Portable Network Graphics (APNG).
    case apng
    /**
     * The Core Foundation string identifier for the image format.
     *
     * This property provides the appropriate UTType identifier for the format,
     * handling both modern (iOS 14.0+) and legacy (pre-iOS 14.0) systems.
     *
     * - Returns: The CFString identifier for the image format.
     */
    var identifier: CFString {
        switch self {
        case .gif:
            if #available(iOS 14.0, macOS 11.0, tvOS 14.0, watchOS 7.0, *) {
                return UTType.gif.identifier as CFString
            } else {
                return kUTTypeGIF
            }
        case .apng:
            if #available(iOS 14.0,macOS 11.0, tvOS 14.0, watchOS 7.0, *) {
                return UTType.png.identifier as CFString
            } else {
                return kUTTypePNG
            }
        }
    }
}
